
def area(a):
    '''
    Функция для вычисления площади квадрата:
    Параметры:
        a (int) - длина стороны квадрата
    Возвращаемое значение:
        a*a - площадь квадрата 
    '''
    return a * a


def perimeter(a):
    '''
    Функция для вычисления периметра квадрата:
    Параметры:
        a (int) - длина стороны квадрата
    Возвращаемое значение:
        4 * a - периметр квадрата 
    '''
    return 4 * a
