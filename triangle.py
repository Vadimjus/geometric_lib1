def area(a, b, c):
    '''
    Функция для вычисления полупериметра треугольника:
    Параметры:
        a, b, c (int) - стороны треугольника
    Возвращаемое значение:
       (a + b + c) / 2 - полупериметр 
    '''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''
    Функция для вычисления периметра треугольника:
    Параметры:
        a, b, c (int) - стороны треугольника
    Возвращаемое значение:
       a + b + c - периметр
    '''
    return a + b + c
